//   [1,2,3,4]
//   [2,3,4,5]
//   результат
//   [3,5,7,9]
/////////////////////////////////////////////////////////////////////////////////////////
function NewArray(){
    let a = 0;
    let maxarray = 0;
    let elem;
    for(key in arguments){
        if (arguments[key].length>maxarray){
            maxarray=arguments[key].length;
            elem = key;
        }
    }
    let newarr = arguments[elem];
    for(key in arguments){
        a++
    }
    for(i = 0; i < a ; i++ ){
        if(i==elem){
            continue
        }
        for(j = 0; j < arguments[i].length; j++){
            newarr[j] = newarr[j] + arguments[i][j]; 
        }
    }
    console.log(newarr)
}
